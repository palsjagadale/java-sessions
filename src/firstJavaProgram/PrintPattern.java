package firstJavaProgram;

public class PrintPattern {

	public static void main(String[] args) {

		for (int i = 0; i <= 3; i++) {

			for (int j = 0; j <= 9; j++) {
				System.out.println(i + "" + j);

			}

		}

		for (int c = 4; c >= 0; c--) {
			System.out.println("n" + " " + "=" + " " + c);

		}

	}

}
